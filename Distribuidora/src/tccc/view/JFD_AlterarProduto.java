    private void EntrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_EntrarMouseClicked
        Produto p = new Produto();

        p.setId_produto(Integer.parseInt(txtid.getText()));
        p.setDescricao(txtdescricao.getText());
        p.setMarca(String.valueOf(cbmarca.getSelectedItem()));
        p.setEstoque_minimo(Integer.parseInt(txtestoque.getText()));

        ProdutoDAO dao = new ProdutoDAO();
        dao.Alterar(p);
        dao.ListarTabela(Tbl);

        Component comp = SwingUtilities.getRoot(this);
        ((Window) comp).dispose();

        ControleDeColuna c = new ControleDeColuna(Tbl, true);
    }//GEN-LAST:event_EntrarMouseClicked

