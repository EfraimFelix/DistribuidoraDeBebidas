    public void AdicionarComboBox() {
        MarcaDAO d = new MarcaDAO();
        List<Marca> marcas = d.getLista();
        DefaultComboBoxModel model = (DefaultComboBoxModel) cbmarca.getModel();
        model.removeAllElements();
        for (Marca c : marcas) {
            model.addElement(c.getDescricao());
        }
    }

