    private void TblPedidoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TblPedidoMouseClicked
        try {
            Menu menu = new Menu();
            if (evt.getClickCount() >= 2) {
                PedidoVender pv = new PedidoVender();
                int linha = TblPedido.getSelectedRow();
                int Id = 0;
                if (linha != -1) {
                    Id = Integer.parseInt(TblPedido.getValueAt(linha, 0).toString());
                    dao.BuscarPedidoVenderID(Id, pv);
                    JFD_VisualizarPedidoVenda VPV = new JFD_VisualizarPedidoVenda(
                            menu,
                            true,
                            String.valueOf(TblPedido.getValueAt(linha, 0)),
                            String.valueOf(pv.getId_cliente()),
                            String.valueOf(pv.getCliente_nome()),
                            String.valueOf(pv.getId_funcionario()),
                            String.valueOf(pv.getFuncionario_nome()),
                            String.valueOf(pv.getTipovenda()),
                            String.valueOf(pv.getStatusentrega()),
                            String.valueOf(pv.getStatuspagamento()),
                            String.valueOf(pv.getData()),
                            String.valueOf(pv.getHora()),
                            String.valueOf(String.format("%.2f", pv.getFrete())),
                            String.valueOf(String.format("%.2f", pv.getDesconto())),
                            String.valueOf(String.format("%.2f", pv.getValor_totalpedido())));
                    VPV.setVisible(true);
                    VPV.setFocusableWindowState(true);
                } else if (linha != -0) {
                    JOptionPane.showMessageDialog(null, "Por favor, clique em algum cadastro");
                }
            }
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, erro);
        }
    }//GEN-LAST:event_TblPedidoMouseClicked

