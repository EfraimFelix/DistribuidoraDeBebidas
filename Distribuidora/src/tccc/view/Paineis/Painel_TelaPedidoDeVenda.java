    private void BotaoAtualizarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BotaoAtualizarMouseClicked
        try {
            if (System.getProperty("Usuario_TipoConta").equals("Administrador")) {
                int linha = TblPedido.getSelectedRow();
                if (linha != -1) {
                    PedidoVenderDAO dao = new PedidoVenderDAO();
                    PedidoVender pv = new PedidoVender();
                    int id = Integer.parseInt(String.valueOf(TblPedido.getValueAt(linha, 0)));
                    dao.BuscarPedidoVenderID(id, pv);
                    if (pv.getStatuspagamento().equals("Pago") || pv.getStatusentrega().equals("Entregue")) {
                        JOptionPane.showMessageDialog(null, "Este pedido já foi confirmado, e agora não pode ser alterado");
                    } else {
                        Menu menu = new Menu();
                        JFD_AlterarPedidoVenda APC = new JFD_AlterarPedidoVenda(
                                menu,
                                true,
                                TblPedido,
                                id,
                                pv.getId_cliente(),
                                pv.getCliente_nome(),
                                pv.getId_funcionario(),
                                pv.getFuncionario_nome(),
                                String.format("%.2f",pv.getFrete()),
                                String.format("%.2f",pv.getDescontoporcentagem()),
                                String.format("%.2f",pv.getDesconto()),
                                String.format("%.2f",pv.getValor_totalpedido()),
                                pv.getTipovenda());
                        APC.setVisible(true);
                    }
                } else if (linha != -0) {
                    JOptionPane.showMessageDialog(null, "Por favor, clique em algum cadastro");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Desculpa, mas apenas administradores tem acesso a está página");
            }
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, erro);
        }
    }//GEN-LAST:event_BotaoAtualizarMouseClicked


