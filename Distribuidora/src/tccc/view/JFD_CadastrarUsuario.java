    private void EntrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_EntrarMouseClicked
        try {
            if (verifica() == true) {
                String tipo = null;
                if (rbadm.isSelected() == true) {
                    tipo = "Administrador";
                } else if (rbcomum.isSelected() == true) {
                    tipo = "Comum";
                } else {
                    tipo = null;
                }
                Usuario u = new Usuario();
                UsuarioDAO dao = new UsuarioDAO();
                if (tipo == null) {
                    JOptionPane.showMessageDialog(null, "Selecione um tipo de conta");
                } else {
                    if (rbadm.isSelected()) {
                        tipo = "Administrador";
                    } else if (rbcomum.isSelected()) {
                        tipo = "Comum";
                    }

                    u.setNome(txtnome.getText());
                    u.setEmail(txtemail.getText());
                    u.setTipoconta(tipo);
                    u.setSenha(String.valueOf(txtnovasenha.getPassword()));
                    dao.adicionar(u);
                    dao.ListarTabela(Tbl);

                    Component comp = SwingUtilities.getRoot(this);
                    ((Window) comp).dispose();
                }
                ControleDeColuna c = new ControleDeColuna(Tbl, true);
            }
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, erro);
        }
    }//GEN-LAST:event_EntrarMouseClicked

