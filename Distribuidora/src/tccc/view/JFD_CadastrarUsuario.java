    public boolean verifica() {
        boolean verifica = true;
        if (txtnome.getText().trim().equals("")) {
            verifica = false;
            JOptionPane.showMessageDialog(null, "Coloque um nome de usuário");
        } else if (String.valueOf(txtnovasenha.getPassword()).trim().equals("")) {
            verifica = false;
            JOptionPane.showMessageDialog(null, "Coloque uma senha");
        } else if (!String.valueOf(txtnovasenha.getPassword()).trim().equals(String.valueOf(txtconfirmasenha.getPassword()))) {
            verifica = false;
            JOptionPane.showMessageDialog(null, "Senhas não se coincidem");
        }
        return verifica;
    }

